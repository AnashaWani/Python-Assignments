### AGGREGATIONS ###


import numpy as np

array = np.array([2,4,6,8])
print("Array :\n",array)

# calculating sum, mean and max of the array #
sum = np.sum(array)
mean = np.mean(array)
max = np.max(array)

print("Sum of the values in array :\n",sum)
print("Mean of the values in array :\n",mean)
print("Maximum value in the array :\n",max)