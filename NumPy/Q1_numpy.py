### IMPORING AND CREATING ARRAY FROM LIST ###


# importing 'numpy' library (for fast mathematical computations, arrays, and numerical operations) #
import numpy as np

# creating list #
list = [1,2,3,4,5]

# converting list into arrays #
array = np.array(list)

# printing the results #
print("Array created by converting list :\n", array)
print("Checking the datatype :\n", type(array))